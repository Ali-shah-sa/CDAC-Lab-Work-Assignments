#include<iostream>
using namespace std;

int main()
{
	int a, b;
	a = 4;
	b = 6;

	cout << " addition of two numbers is " << (a + b) << endl;
	cout << " sub of two numbers is " << (a - b) << endl;
	cout << " mul of two numbers is " << (a * b) << endl;
	cout << " div of two numbers is " << (a / b) << endl;
	cout << " mod of two numbers is " << (a % b) << endl;
	return 0;
	 

}